#include <iostream>

using namespace std;

int main() {
    /*
        Basic Data Types:
    */

    int a = 0;        // a number between -2 ^ 31 and 2 ^ 31
    bool b = true;    // true or false
    char c = 'c';     // ONE character
    long d = 3;       // a number between -2 ^ 63 and 2 ^ 63 - 1

    /* 
        Floating Point Data Types: 
    */

    float e = 2.3;    // a decimal number between -3.4 * 10 ^ 38 and 3.4 * 10 ^ 38
    double f = 2.333; // a decimal number between -1.7 * 10 ^ 308 and 1.7 * 10 ^ 308

    //*********************************************************
    // NOTE:
    // There is one more difference between float and double
    // and that is that double can hold 15 significant figures
    // or more but float can hold only 6 or 7.
    //*********************************************************

    
}